    @staticmethod
    def _option_dte(expiry: str | None, as_of_date: str) -> int | None:
        if not expiry:
            return None
        try:
            exp_date = datetime.strptime(str(expiry), "%Y-%m-%d").date()
            base_date = datetime.strptime(as_of_date, "%Y-%m-%d").date()
        except ValueError:
            return None
        return (exp_date - base_date).days

    def _estimate_polygon_atm_iv(
        self,
        rows: list[dict[str, Any]],
        spot_price: float | None,
    ) -> float | None:
        if not self._is_valid(spot_price):
            return None
        now = datetime.now(timezone.utc).date()
        candidates: list[tuple[float, float, int]] = []

        for row in rows:
            details = row.get("details") or {}
            expiry = details.get("expiration_date")
            strike = self._safe_float(details.get("strike_price"))
            iv = self._safe_float(row.get("implied_volatility"))
            if (
                not expiry
                or not self._is_valid(strike)
                or not self._is_valid(iv)
            ):
                continue
            try:
                exp_date = datetime.strptime(expiry, "%Y-%m-%d").date()
            except ValueError:
                continue
            dte = (exp_date - now).days
            if dte < 10 or dte > 60:
                continue
            strike_distance = abs((strike or 0.0) - (spot_price or 0.0))
            dte_distance = abs(dte - 30)
            candidates.append((strike_distance, iv or 0.0, dte_distance))

        if not candidates:
            return None
        candidates.sort(key=lambda x: (x[2], x[0]))
        top = [item[1] for item in candidates[:8]]
        return self._safe_float(float(pd.Series(top).median()))


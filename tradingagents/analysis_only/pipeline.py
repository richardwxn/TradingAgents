    def _resolve_factor_weights(
        self,
        *,
        market_context: dict[str, Any] | None = None,
    ) -> dict[str, float]:
        """Resolve effective per-factor weights for one report.

        If `regime_weights_path` was supplied to the constructor and the
        file exists, look up the regime via `regime_for_market_context`
        and use the matching weight vector when present. Falls back to
        the standard `resolve_factor_weights(self.factor_weights)` path
        whenever the regime is unknown or absent from the config — the
        Phase 4 "ship only when sufficient samples" gate is enforced
        upstream (the tuner writes only ship-able regimes into the file).
        """
        regime_map = self._load_regime_weights_map()
        if regime_map and market_context is not None:
            from tradingagents.analysis_only.scoring import (
                REGIME_UNKNOWN,
                regime_for_market_context,
            )

            regime = regime_for_market_context(market_context)
            if regime != REGIME_UNKNOWN:
                regime_weights = regime_map.get(regime)
                if regime_weights:
                    return resolve_factor_weights(regime_weights)
        return resolve_factor_weights(self.factor_weights)


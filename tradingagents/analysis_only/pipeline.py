    @staticmethod
    def _compute_rsi(close: pd.Series, period: int) -> float:
        delta = close.diff()
        gain = delta.clip(lower=0).rolling(period).mean()
        loss = (-delta.clip(upper=0)).rolling(period).mean()
        rs = gain / loss.replace(0, pd.NA)
        rsi = 100 - (100 / (1 + rs))
        value = rsi.iloc[-1]
        return float(value) if pd.notna(value) else float("nan")

    @staticmethod
    def _compute_atr(
        high: pd.Series,
        low: pd.Series,
        close: pd.Series,
        period: int = 14,
    ) -> float:
        prev_close = close.shift(1)
        tr = pd.concat(
            [
                (high - low).abs(),
                (high - prev_close).abs(),
                (low - prev_close).abs(),
            ],
            axis=1,
        ).max(axis=1)
        value = tr.rolling(period).mean().iloc[-1]
        return float(value) if pd.notna(value) else float("nan")

    def _build_delta_block(
        self,
        as_of_date: str,
        new_close: float | None,
        new_direction: str,
        new_confidence: float,
        new_composite: float,
        new_options_unusual: int,
        new_factor_scores: list[dict[str, Any]],
    ) -> dict[str, Any]:
        if not self._state_store:
            return {"status": "state_store_disabled"}
        prev = self._prev_state
        if prev is None or prev.last_run_time is None:
            return {"status": "first_report"}

        def _delta(a: Any, b: Any) -> float | None:
            if a is None or b is None:
                return None
            try:
                return round(float(a) - float(b), 6)
            except (TypeError, ValueError):
                return None

        def _pct(a: Any, b: Any) -> float | None:
            if a is None or b is None:
                return None
            try:
                base = float(b)
            except (TypeError, ValueError):
                return None
            if base == 0:
                return None
            try:
                return round((float(a) / base) - 1.0, 6)
            except (TypeError, ValueError):
                return None

        prev_factors_by_name: dict[str, dict[str, Any]] = {
            str(f.get("factor")): f
            for f in (prev.last_factor_scores or [])
            if isinstance(f, dict) and f.get("factor")
        }
        flips: list[dict[str, Any]] = []
        top_movers: list[dict[str, Any]] = []
        for f in new_factor_scores:
            name = f.get("factor")
            if not name:
                continue
            prev_f = prev_factors_by_name.get(name)
            if not prev_f:
                continue
            prev_bucket = prev_f.get("bucket")
            new_bucket = f.get("bucket")
            if prev_bucket and new_bucket and prev_bucket != new_bucket:
                flips.append(
                    {
                        "factor": name,
                        "from_bucket": prev_bucket,
                        "to_bucket": new_bucket,
                        "rationale": f.get("rationale"),
                    }
                )
            ws_delta = _delta(
                f.get("weighted_score"), prev_f.get("weighted_score")
            )
            if ws_delta is not None and abs(ws_delta) >= 0.005:
                top_movers.append(
                    {
                        "factor": name,
                        "pillar": f.get("pillar"),
                        "weighted_score_delta": ws_delta,
                        "from": prev_f.get("weighted_score"),
                        "to": f.get("weighted_score"),
                        "rationale": f.get("rationale"),
                    }
                )
        top_movers.sort(
            key=lambda x: abs(x["weighted_score_delta"]),
            reverse=True,
        )

        return {
            "status": "ok",
            "previous_run_at_utc": prev.last_run_time,
            "previous_as_of_date": prev.last_as_of_date,
            "previous_direction": prev.last_signal,
            "previous_confidence": self._safe_float(prev.last_confidence),
            "previous_close": self._safe_float(prev.last_price),
            "previous_composite_score": self._safe_float(
                prev.last_composite_score
            ),
            "previous_options_unusual_count": prev.last_options_unusual_count,
            "previous_thesis": prev.last_thesis,
            "direction_changed": bool(
                prev.last_signal and prev.last_signal != new_direction
            ),
            "direction_transition": (
                f"{prev.last_signal} -> {new_direction}"
                if prev.last_signal and prev.last_signal != new_direction
                else None
            ),
            "confidence_delta": _delta(new_confidence, prev.last_confidence),
            "close_delta": _delta(new_close, prev.last_price),
            "close_pct_change": _pct(new_close, prev.last_price),
            "composite_score_delta": _delta(
                new_composite, prev.last_composite_score
            ),
            "options_unusual_count_delta": _delta(
                new_options_unusual, prev.last_options_unusual_count
            ),
            "as_of_date_delta_days": self._days_between(
                as_of_date, prev.last_as_of_date
            ),
            "factor_bucket_flips": flips,
            "top_factor_movers": top_movers[:8],
        }

    @staticmethod
    def _days_between(a: str | None, b: str | None) -> int | None:
        if not a or not b:
            return None
        try:
            da = datetime.strptime(a, "%Y-%m-%d").date()
            db = datetime.strptime(b, "%Y-%m-%d").date()
        except ValueError:
            return None
        return (da - db).days

    def _market_now(self) -> datetime:
        """Current time in the primary US equity market timezone."""
        return datetime.now(timezone.utc).astimezone(ZoneInfo("America/New_York"))

    def _current_market_reference_date(self) -> date:
        """Most recent US equity session that should use live snapshots.

        The pipeline previously compared `as_of_date` with the UTC calendar
        day. That made Sunday-night Pacific runs look historical once UTC had
        rolled to Monday, even though the latest completed market session was
        still Friday. Use New York time and walk back before the market opens.
        """
        market_now = self._market_now()
        ref = market_now.date()
        if ref.weekday() >= 5 or market_now.time() < time(9, 30):
            ref -= timedelta(days=1)
        while ref.weekday() >= 5:
            ref -= timedelta(days=1)
        return ref

    def _resolve_pit_mode(self, as_of_date: str) -> str:
        """Return 'live' for the current market reference date or newer."""
        reference_date = self._current_market_reference_date()
        try:
            target = datetime.strptime(as_of_date, "%Y-%m-%d").date()
        except ValueError:
            return "live"
        return "live" if target >= reference_date else "historical"

    def _live_or_leak(self, as_of_date: str) -> str:
        """Status to stamp on sections that always pull realtime data."""
        return (
            "live"
            if self._resolve_pit_mode(as_of_date) == "live"
            else "non_pit_live_snapshot"
        )

    def _set_pit(self, section: str, status: str) -> None:
        self._pit_status[section] = status

    def _pit_warnings(self) -> list[str]:
        return sorted(
            section
            for section, status in self._pit_status.items()
            if status == "non_pit_live_snapshot"
        )

    @staticmethod
    def _as_of_eod_epoch(as_of_date: str) -> int | None:
        try:
            d = datetime.strptime(as_of_date, "%Y-%m-%d")
        except ValueError:
            return None
        eod = d.replace(hour=23, minute=59, second=59, tzinfo=timezone.utc)
        return int(eod.timestamp())

    @staticmethod
    def _news_item_timestamp(item: dict[str, Any]) -> int | None:
        ts = item.get("providerPublishTime")
        if ts is not None:
            try:
                return int(ts)
            except (TypeError, ValueError):
                pass
        content = item.get("content") or {}
        pub = content.get("pubDate") or content.get("displayTime")
        if pub:
            try:
                iso = str(pub).replace("Z", "+00:00")
                return int(datetime.fromisoformat(iso).timestamp())
            except Exception:
                return None
        return None

    @staticmethod
    def _epoch_to_utc(epoch_seconds: int | None) -> str | None:
        if epoch_seconds is None:
            return None
        try:
            return datetime.fromtimestamp(
                int(epoch_seconds),
                tz=timezone.utc,
            ).isoformat()
        except Exception:
            return None

    @staticmethod
    def _safe_float(value: Any) -> float | None:
        if value is None:
            return None
        try:
            val = float(value)
        except (TypeError, ValueError):
            return None
        if math.isnan(val) or math.isinf(val):
            return None
        return round(val, 6)

    @staticmethod
    def _is_valid(value: float | None) -> bool:
        return value is not None

    def _log(self, msg: str) -> None:
        if self.verbose:
            print(f"[analysis-mvp] {msg}")
        if self.logger:
            self.logger.info(msg)

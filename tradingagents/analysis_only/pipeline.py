    def _file_fingerprint(self, path: str | None) -> dict[str, Any] | None:
        if not path:
            return None
        p = Path(path)
        if not p.exists():
            return {"path": str(p), "exists": False}
        try:
            return {
                "path": str(p),
                "exists": True,
                "sha256": stable_hash(p.read_text()),
            }
        except Exception:
            stat = p.stat()
            return {
                "path": str(p),
                "exists": True,
                "mtime_ns": stat.st_mtime_ns,
                "size": stat.st_size,
            }

    def _persist_state_for(
        self,
        symbol: str,
        report: AnalysisReport,
    ) -> None:
        kf = report.key_features or {}
        scoring = kf.get("model_scoring") or {}
        tech = kf.get("technical") or {}
        opts = kf.get("options_flow") or {}
        assert self._state_store is not None
        self._state_store.upsert_symbol_state(
            symbol=symbol,
            last_price=self._safe_float(tech.get("close")),
            last_signal=report.direction,
            last_composite_score=self._safe_float(
                scoring.get("composite_score")
            ),
            last_options_unusual_count=int(opts.get("unusual_count") or 0),
            last_as_of_date=report.as_of_date,
            last_confidence=self._safe_float(report.confidence),
            last_factor_scores=scoring.get("factor_scores"),
            last_thesis=report.thesis,
        )
        iv = kf.get("options_iv") or {}
        if iv.get("status") == "ok":
            self._state_store.record_iv_snapshot(
                symbol=symbol,
                as_of_date=report.as_of_date,
                atm_iv_30d=self._safe_float(iv.get("atm_iv_30d")),
                atm_iv_60d=self._safe_float(iv.get("atm_iv_60d")),
                atm_iv_90d=self._safe_float(iv.get("atm_iv_90d")),
                skew_25d_30d=self._safe_float(iv.get("skew_25d_30d")),
                term_slope_30_to_60=self._safe_float(
                    iv.get("term_structure_slope_30_to_60")
                ),
            )

    def save_report(
        self, report: AnalysisReport, output_dir: str | Path
    ) -> Path:
        output_path = Path(output_dir)
        output_path.mkdir(parents=True, exist_ok=True)
        file_path = output_path / f"{report.symbol}_{report.as_of_date}.json"
        file_path.write_text(json.dumps(report.to_json_dict(), indent=2))
        return file_path

    def _cache_ttl(
        self,
        as_of_date: str,
        *,
        live_seconds: int = 900,
    ) -> int | None:
        return (
            live_seconds
            if self._resolve_pit_mode(as_of_date) == "live"
            else None
        )

    def _cache_key(self, kind: str, payload: dict[str, Any]) -> str:
        return stable_hash(
            {
                "schema": CACHE_SCHEMA_VERSION,
                "kind": kind,
                "payload": payload,
            }
        )

    def _get_cached_json(
        self,
        namespace: str,
        kind: str,
        payload: dict[str, Any],
        *,
        ttl_seconds: int | None,
    ) -> Any | None:
        if self.cache is None:
            return None
        return self.cache.get_json(
            namespace,
            self._cache_key(kind, payload),
            ttl_seconds=ttl_seconds,
        )

    def _set_cached_json(
        self,
        namespace: str,
        kind: str,
        payload: dict[str, Any],
        value: Any,
    ) -> None:
        if self.cache is None:
            return
        self.cache.set_json(namespace, self._cache_key(kind, payload), value)

    def _get_cached_dataframe(
        self,
        namespace: str,
        kind: str,
        payload: dict[str, Any],
        *,
        ttl_seconds: int | None,
    ) -> pd.DataFrame | None:
        if self.cache is None:
            return None
        return self.cache.get_dataframe(
            namespace,
            self._cache_key(kind, payload),
            ttl_seconds=ttl_seconds,
        )

    def _set_cached_dataframe(
        self,
        namespace: str,
        kind: str,
        payload: dict[str, Any],
        value: pd.DataFrame,
    ) -> None:
        if self.cache is None or value.empty:
            return
        self.cache.set_dataframe(
            namespace,
            self._cache_key(kind, payload),
            value,
        )

    def _load_price_data(
        self, symbol: str, as_of_date: str, lookback_days: int
    ) -> pd.DataFrame:
        if self.data_provider in ("auto", "polygon"):
            if self.polygon_api_key:
                self._log("Trying Polygon price source...")
                polygon_df = self._load_price_data_polygon(
                    symbol, as_of_date, lookback_days
                )
                if polygon_df is not None and not polygon_df.empty:
                    self.resolved_provider = "polygon"
                    self._log("Polygon price source succeeded.")
                    return polygon_df
                self._log(
                    "Polygon unavailable or returned no data; falling back."
                )
            elif self.data_provider == "polygon":
                self._log("POLYGON_API_KEY not set; cannot use Polygon.")

        if self.data_provider in ("auto", "openbb"):
            self._log("Trying OpenBB price source...")
            openbb_df = self._load_price_data_openbb(
                symbol, as_of_date, lookback_days
            )
            if openbb_df is not None and not openbb_df.empty:
                self.resolved_provider = "openbb"
                self._log("OpenBB price source succeeded.")
                return openbb_df
            self._log("OpenBB unavailable or returned no data; falling back.")
        self.resolved_provider = "yfinance"
        self._log("Using yfinance price source.")
        return self._load_price_data_yfinance(
            symbol, as_of_date, lookback_days
        )

    def _load_price_data_polygon(
        self, symbol: str, as_of_date: str, lookback_days: int
    ) -> pd.DataFrame | None:
        if not self.polygon_api_key:
            return None
        end_dt = datetime.strptime(as_of_date, "%Y-%m-%d")
        start_dt = end_dt - timedelta(days=lookback_days)
        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "lookback_days": lookback_days,
            "start": start_dt.strftime("%Y-%m-%d"),
            "end": end_dt.strftime("%Y-%m-%d"),
            "provider": "polygon",
            "adjusted": True,
        }
        cached = self._get_cached_dataframe(
            "price_data",
            "daily_bars",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if cached is not None and not cached.empty:
            self._log("Polygon price source cache hit.")
            return cached
        url = (
            "https://api.polygon.io/v2/aggs/ticker/"
            f"{symbol}/range/1/day/"
            f"{start_dt.strftime('%Y-%m-%d')}/{end_dt.strftime('%Y-%m-%d')}"
        )
        params = {
            "adjusted": "true",
            "sort": "asc",
            "limit": 50000,
            "apiKey": self.polygon_api_key,
        }
        try:
            response = requests.get(url, params=params, timeout=20)
            response.raise_for_status()
            payload = response.json()
        except Exception as exc:
            self._log(f"Polygon price request failed: {exc}")
            return None
        rows = payload.get("results") or []
        if not rows:
            return None
        data = pd.DataFrame(rows)
        data = data.rename(
            columns={
                "t": "Date",
                "o": "Open",
                "h": "High",
                "l": "Low",
                "c": "Close",
                "v": "Volume",
            }
        )
        data["Date"] = pd.to_datetime(data["Date"], unit="ms", utc=True)
        data["Date"] = data["Date"].dt.tz_localize(None)
        out = data[["Date", "Open", "High", "Low", "Close", "Volume"]]
        self._set_cached_dataframe(
            "price_data",
            "daily_bars",
            cache_payload,
            out,
        )
        return out

    def _load_price_data_openbb(
        self, symbol: str, as_of_date: str, lookback_days: int
    ) -> pd.DataFrame | None:
        if self.data_provider == "yfinance":
            return None
        try:
            from openbb import obb  # type: ignore
        except Exception:
            return None

        end_dt = datetime.strptime(as_of_date, "%Y-%m-%d")
        start_dt = end_dt - timedelta(days=lookback_days)
        try:
            # OpenBB API signatures can vary by installed version/provider.
            result = obb.equity.price.historical(
                symbol=symbol,
                start_date=start_dt.strftime("%Y-%m-%d"),
                end_date=(end_dt + timedelta(days=1)).strftime("%Y-%m-%d"),
            )
            df = result.to_df()
            if df.empty:
                return None
            return self._normalize_price_df(df.reset_index())
        except Exception:
            return None

    def _load_price_data_yfinance(
        self, symbol: str, as_of_date: str, lookback_days: int
    ) -> pd.DataFrame:
        end_dt = datetime.strptime(as_of_date, "%Y-%m-%d")
        start_dt = end_dt - timedelta(days=lookback_days)
        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "lookback_days": lookback_days,
            "start": start_dt.strftime("%Y-%m-%d"),
            "end": (end_dt + timedelta(days=1)).strftime("%Y-%m-%d"),
            "provider": "yfinance",
            "adjusted": True,
        }
        cached = self._get_cached_dataframe(
            "price_data",
            "daily_bars",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if cached is not None and not cached.empty:
            self._log("yfinance price source cache hit.")
            return cached
        data = yf.download(
            symbol,
            start=start_dt.strftime("%Y-%m-%d"),
            end=(end_dt + timedelta(days=1)).strftime("%Y-%m-%d"),
            auto_adjust=True,
            progress=False,
            multi_level_index=False,
        )
        if data.empty:
            raise ValueError(
                f"No price data found for {symbol} up to {as_of_date}"
            )
        out = self._normalize_price_df(data.reset_index())
        self._set_cached_dataframe(
            "price_data",
            "daily_bars",
            cache_payload,
            out,
        )
        return out

    def _normalize_price_df(self, df: pd.DataFrame) -> pd.DataFrame:
        rename_map = {c: c.capitalize() for c in df.columns}
        df = df.rename(columns=rename_map)
        expected = ["Date", "Open", "High", "Low", "Close", "Volume"]
        missing = [col for col in expected if col not in df.columns]
        if missing:
            raise ValueError(f"Price data missing required columns: {missing}")
        return df

    def _load_fundamentals(
        self,
        ticker: Any,
        as_of_date: str,
        spot_close: float | None = None,
    ) -> dict[str, float | None]:
        is_historical = self._resolve_pit_mode(as_of_date) == "historical"

        fin_provider, fin_name = self._active_financials_provider()
        if is_historical and fin_provider is not None:
            pit_fields = self._load_fundamentals_pit(
                provider=fin_provider,
                provider_name=fin_name,
                ticker=ticker,
                as_of_date=as_of_date,
                spot_close=spot_close,
            )
            if pit_fields is not None:
                self._set_pit("fundamentals", "pit")
                self._set_pit("fundamentals.source", f"{fin_name}_pit")
                return {
                    k: self._safe_float(v) for k, v in pit_fields.items()
                }
            self._log(
                f"{fin_name} financials returned no PIT data; "
                "marking fundamentals unavailable (no PIT fallback for "
                "historical dates)."
            )

        # Phase 1 corpus rule: historical dates without a PIT fundamentals
        # source must NOT fall back to live ticker.info — that would leak
        # current TTM into the composite via fund_* factors. Return all-None
        # so the affected factors register as data_available=False.
        if is_historical:
            self._set_pit("fundamentals", "disabled_historical")
            self._set_pit("fundamentals.source", "none_pit_only")
            return {k: None for k in (
                "market_cap", "enterprise_value", "enterprise_to_revenue",
                "enterprise_to_ebitda", "trailing_pe", "forward_pe",
                "price_to_book", "price_to_sales", "profit_margins",
                "operating_margins", "gross_margins", "return_on_equity",
                "return_on_assets", "debt_to_equity", "current_ratio",
                "quick_ratio", "revenue_growth", "earnings_growth",
                "free_cashflow", "beta",
                "revenue_qoq_growth", "net_income_qoq_growth",
                "operating_income_qoq_growth", "free_cashflow_qoq_growth",
            )}

        symbol_str = (getattr(ticker, "ticker", "") or "").upper()
        cache_payload = {
            "symbol": symbol_str,
            "as_of_date": as_of_date,
            "provider": "yfinance_live_snapshot",
        }
        cached = self._get_cached_json(
            "fundamentals",
            "live_snapshot_fields",
            cache_payload,
            ttl_seconds=3600,
        )
        if isinstance(cached, dict):
            self._set_pit("fundamentals", self._live_or_leak(as_of_date))
            self._set_pit("fundamentals.source", "yfinance_live_snapshot")
            return {k: self._safe_float(v) for k, v in cached.items()}

        try:
            info = ticker.info or {}
        except Exception:
            info = {}
        fields = {
            "market_cap": info.get("marketCap"),
            "enterprise_value": info.get("enterpriseValue"),
            "enterprise_to_revenue": info.get("enterpriseToRevenue"),
            "enterprise_to_ebitda": info.get("enterpriseToEbitda"),
            "trailing_pe": info.get("trailingPE"),
            "forward_pe": info.get("forwardPE"),
            "price_to_book": info.get("priceToBook"),
            "price_to_sales": info.get("priceToSalesTrailing12Months"),
            "profit_margins": info.get("profitMargins"),
            "operating_margins": info.get("operatingMargins"),
            "gross_margins": info.get("grossMargins"),
            "return_on_equity": info.get("returnOnEquity"),
            "return_on_assets": info.get("returnOnAssets"),
            "debt_to_equity": info.get("debtToEquity"),
            "current_ratio": info.get("currentRatio"),
            "quick_ratio": info.get("quickRatio"),
            "revenue_growth": info.get("revenueGrowth"),
            "earnings_growth": info.get("earningsGrowth"),
            "free_cashflow": info.get("freeCashflow"),
            "beta": info.get("beta"),
        }

        trend_fields = self._extract_statement_trends(ticker, as_of_date)
        fields.update(trend_fields)
        self._set_pit("fundamentals", self._live_or_leak(as_of_date))
        self._set_pit("fundamentals.source", "yfinance_live_snapshot")
        out = {k: self._safe_float(v) for k, v in fields.items()}
        self._set_cached_json(
            "fundamentals",
            "live_snapshot_fields",
            cache_payload,
            out,
        )
        return out

    def _active_financials_provider(self) -> tuple[Any | None, str]:
        """Resolve the (provider, name) used for PIT fundamentals.

        ``fundamentals_provider`` selects the source independently of the price
        ``data_provider``. "auto" preserves the prior behavior (Polygon when
        available), falling back to FMP; "fmp"/"polygon" force a specific one.
        The name is used in the PIT label and cache key so the two sources
        never share cached entries.
        """
        pref = self.fundamentals_provider
        if pref == "fmp" and self.fmp_financials_provider is not None:
            return self.fmp_financials_provider, "fmp_financials"
        if pref in ("auto", "polygon") and self.polygon_financials_provider is not None:
            return self.polygon_financials_provider, "polygon_financials"
        if pref == "auto" and self.fmp_financials_provider is not None:
            return self.fmp_financials_provider, "fmp_financials"
        return None, ""

    def _load_fundamentals_pit(
        self,
        provider: Any,
        provider_name: str,
        ticker: Any,
        as_of_date: str,
        spot_close: float | None = None,
    ) -> dict[str, float | None] | None:
        """PIT fundamentals via a financials provider + price-derived ratios.

        Works with any provider returning Polygon-shaped records
        (PolygonFinancialsProvider / FMPFinancialsProvider). Returns None if
        the provider returned no usable data, so the caller can fall back.
        Keys mirror the legacy `_load_fundamentals` shape so downstream factor
        scoring is unchanged.
        """
        if provider is None:
            return None

        symbol_str = getattr(ticker, "ticker", "") or ""
        cache_payload = {
            "symbol": symbol_str.upper(),
            "as_of_date": as_of_date,
            "spot_close": round(float(spot_close), 6)
            if self._is_valid(spot_close)
            else None,
            "provider": f"{provider_name}_pit",
        }
        cached = self._get_cached_json(
            "fundamentals",
            "polygon_pit_fields",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=3600),
        )
        if isinstance(cached, dict):
            return {k: self._safe_float(v) for k, v in cached.items()}
        try:
            quarterly = provider.fetch_quarterly(
                symbol=symbol_str,
                as_of_date=as_of_date,
                limit=8,
            )
            annuals = provider.fetch_annual(
                symbol=symbol_str,
                as_of_date=as_of_date,
                limit=3,
            )
        except Exception as exc:
            self._log(f"{provider_name} financials fetch error: {exc}")
            return None

        if not quarterly and not annuals:
            return None

        latest_q = quarterly[0] if quarterly else None
        prev_q = quarterly[1] if len(quarterly) >= 2 else None
        # Polygon's quarterly stream omits Q4 (rolled into 10-K) and sometimes
        # has additional gaps. Synthesizing TTM by summing 4 sequential
        # quarterlies is unreliable. Instead, use the latest annual as TTM
        # (always PIT, sometimes stale) and the prior annual as TTM-prev for
        # YoY growth. Quarterly records remain authoritative for QoQ deltas.
        latest_ttm = annuals[0] if annuals else None
        prev_ttm = annuals[1] if len(annuals) >= 2 else None

        fields: dict[str, float | None] = {
            "market_cap": None,
            "enterprise_value": None,
            "trailing_pe": None,
            "forward_pe": None,
            "price_to_book": None,
            "price_to_sales": None,
            "profit_margins": None,
            "operating_margins": None,
            "gross_margins": None,
            "return_on_equity": None,
            "return_on_assets": None,
            "debt_to_equity": None,
            "current_ratio": None,
            "quick_ratio": None,
            "revenue_growth": None,
            "earnings_growth": None,
            "free_cashflow": None,
            "beta": None,
            "revenue_qoq_growth": None,
            "net_income_qoq_growth": None,
            "operating_income_qoq_growth": None,
            "free_cashflow_qoq_growth": None,
        }

        ttm_revenue = self._pf_pick(
            latest_ttm, "income_statement", ["revenues", "revenue"]
        )
        ttm_net_income = self._pf_pick(
            latest_ttm,
            "income_statement",
            ["net_income_loss", "net_income_loss_attributable_to_parent"],
        )
        ttm_operating_income = self._pf_pick(
            latest_ttm, "income_statement", ["operating_income_loss"]
        )
        ttm_gross_profit = self._pf_pick(
            latest_ttm, "income_statement", ["gross_profit"]
        )
        if ttm_revenue:
            if ttm_net_income is not None:
                fields["profit_margins"] = ttm_net_income / ttm_revenue
            if ttm_operating_income is not None:
                fields["operating_margins"] = ttm_operating_income / ttm_revenue
            if ttm_gross_profit is not None:
                fields["gross_margins"] = ttm_gross_profit / ttm_revenue

        equity = self._pf_pick(
            latest_q, "balance_sheet", ["equity", "equity_attributable_to_parent"]
        )
        assets = self._pf_pick(latest_q, "balance_sheet", ["assets"])
        liabilities = self._pf_pick(latest_q, "balance_sheet", ["liabilities"])
        current_assets = self._pf_pick(
            latest_q, "balance_sheet", ["current_assets"]
        )
        current_liabilities = self._pf_pick(
            latest_q, "balance_sheet", ["current_liabilities"]
        )
        inventory = self._pf_pick(latest_q, "balance_sheet", ["inventory"])

        if equity and equity > 0:
            if ttm_net_income is not None:
                fields["return_on_equity"] = ttm_net_income / equity
            if liabilities is not None:
                fields["debt_to_equity"] = liabilities / equity
        if assets and assets > 0 and ttm_net_income is not None:
            fields["return_on_assets"] = ttm_net_income / assets
        if current_liabilities and current_liabilities > 0:
            if current_assets is not None:
                fields["current_ratio"] = current_assets / current_liabilities
            quick_assets = (
                (current_assets or 0.0) - (inventory or 0.0)
                if current_assets is not None
                else None
            )
            if quick_assets is not None:
                fields["quick_ratio"] = quick_assets / current_liabilities

        ttm_op_cf = self._pf_pick(
            latest_ttm,
            "cash_flow_statement",
            [
                "net_cash_flow_from_operating_activities",
                "net_cash_flow_from_operating_activities_continuing",
            ],
        )
        ttm_capex = self._pf_pick(
            latest_ttm,
            "cash_flow_statement",
            [
                "payments_for_property_plant_and_equipment",
                "net_cash_flow_from_investing_activities",
            ],
        )
        if ttm_op_cf is not None and ttm_capex is not None:
            # Capex is typically reported as negative; subtract its magnitude.
            fields["free_cashflow"] = ttm_op_cf - abs(ttm_capex)
        elif ttm_op_cf is not None:
            fields["free_cashflow"] = ttm_op_cf

        if prev_ttm is not None:
            prev_revenue = self._pf_pick(
                prev_ttm, "income_statement", ["revenues", "revenue"]
            )
            prev_net_income = self._pf_pick(
                prev_ttm,
                "income_statement",
                ["net_income_loss", "net_income_loss_attributable_to_parent"],
            )
            if (
                ttm_revenue is not None
                and prev_revenue is not None
                and prev_revenue != 0
            ):
                fields["revenue_growth"] = (ttm_revenue / prev_revenue) - 1.0
            if (
                ttm_net_income is not None
                and prev_net_income is not None
                and prev_net_income != 0
            ):
                fields["earnings_growth"] = (
                    ttm_net_income / prev_net_income
                ) - 1.0

        if prev_q is not None:
            q_revenue = self._pf_pick(
                latest_q, "income_statement", ["revenues", "revenue"]
            )
            q_prev_revenue = self._pf_pick(
                prev_q, "income_statement", ["revenues", "revenue"]
            )
            if (
                q_revenue is not None
                and q_prev_revenue is not None
                and q_prev_revenue != 0
            ):
                fields["revenue_qoq_growth"] = (
                    q_revenue / q_prev_revenue
                ) - 1.0

            q_net_income = self._pf_pick(
                latest_q,
                "income_statement",
                ["net_income_loss", "net_income_loss_attributable_to_parent"],
            )
            q_prev_net_income = self._pf_pick(
                prev_q,
                "income_statement",
                ["net_income_loss", "net_income_loss_attributable_to_parent"],
            )
            if (
                q_net_income is not None
                and q_prev_net_income is not None
                and q_prev_net_income != 0
            ):
                fields["net_income_qoq_growth"] = (
                    q_net_income / q_prev_net_income
                ) - 1.0

            q_op_income = self._pf_pick(
                latest_q, "income_statement", ["operating_income_loss"]
            )
            q_prev_op_income = self._pf_pick(
                prev_q, "income_statement", ["operating_income_loss"]
            )
            if (
                q_op_income is not None
                and q_prev_op_income is not None
                and q_prev_op_income != 0
            ):
                fields["operating_income_qoq_growth"] = (
                    q_op_income / q_prev_op_income
                ) - 1.0

            q_op_cf = self._pf_pick(
                latest_q,
                "cash_flow_statement",
                [
                    "net_cash_flow_from_operating_activities",
                    "net_cash_flow_from_operating_activities_continuing",
                ],
            )
            q_capex = self._pf_pick(
                latest_q,
                "cash_flow_statement",
                ["payments_for_property_plant_and_equipment"],
            )
            q_fcf = None
            if q_op_cf is not None and q_capex is not None:
                q_fcf = q_op_cf - abs(q_capex)
            elif q_op_cf is not None:
                q_fcf = q_op_cf

            q_prev_op_cf = self._pf_pick(
                prev_q,
                "cash_flow_statement",
                [
                    "net_cash_flow_from_operating_activities",
                    "net_cash_flow_from_operating_activities_continuing",
                ],
            )
            q_prev_capex = self._pf_pick(
                prev_q,
                "cash_flow_statement",
                ["payments_for_property_plant_and_equipment"],
            )
            q_prev_fcf = None
            if q_prev_op_cf is not None and q_prev_capex is not None:
                q_prev_fcf = q_prev_op_cf - abs(q_prev_capex)
            elif q_prev_op_cf is not None:
                q_prev_fcf = q_prev_op_cf
            if q_fcf is not None and q_prev_fcf is not None and q_prev_fcf != 0:
                fields["free_cashflow_qoq_growth"] = (
                    q_fcf / q_prev_fcf
                ) - 1.0

        if spot_close is None:
            spot_close = self._spot_close_on_or_before(
                symbol=getattr(ticker, "ticker", "") or "",
                as_of_date=as_of_date,
            )
        shares = self._pf_pick(
            latest_q,
            "income_statement",
            [
                "diluted_average_shares",
                "basic_average_shares",
            ],
        )
        if spot_close is not None and shares is not None and shares > 0:
            market_cap = spot_close * shares
            fields["market_cap"] = market_cap

            if ttm_net_income and ttm_net_income > 0:
                eps_ttm = ttm_net_income / shares
                if eps_ttm > 0:
                    fields["trailing_pe"] = spot_close / eps_ttm
            if ttm_revenue and ttm_revenue > 0:
                fields["price_to_sales"] = market_cap / ttm_revenue
            if equity and equity > 0:
                fields["price_to_book"] = market_cap / equity

            cash_and_equivs = self._pf_pick(
                latest_q,
                "balance_sheet",
                ["cash", "cash_and_cash_equivalents"],
            )
            debt = self._pf_pick(
                latest_q,
                "balance_sheet",
                [
                    "long_term_debt",
                    "noncurrent_liabilities",
                ],
            )
            if cash_and_equivs is not None or debt is not None:
                ev = (
                    market_cap
                    + (debt or 0.0)
                    - (cash_and_equivs or 0.0)
                )
                fields["enterprise_value"] = ev

        self._set_cached_json(
            "fundamentals",
            "polygon_pit_fields",
            cache_payload,
            fields,
        )
        return fields

    @staticmethod
    def _pf_synthesize_ttm(
        quarterly_records: list[dict[str, Any]],
    ) -> dict[str, Any] | None:
        """Build a synthetic TTM record by summing flow-statement line items
        across the latest 4 quarterlies. Balance-sheet items are taken from
        the most recent quarter (point-in-time, not summed).

        `quarterly_records` must be sorted newest first.
        """
        if len(quarterly_records) < 4:
            return None
        sum_statements = ["income_statement", "cash_flow_statement"]
        out_financials: dict[str, dict[str, Any]] = {}

        # Sum flow items across the 4 quarters.
        for stmt in sum_statements:
            line_sums: dict[str, float] = {}
            line_units: dict[str, str] = {}
            for q in quarterly_records[:4]:
                section = (q.get("financials") or {}).get(stmt) or {}
                for line_key, line in section.items():
                    if not isinstance(line, dict):
                        continue
                    val = line.get("value")
                    try:
                        v = float(val) if val is not None else None
                    except (TypeError, ValueError):
                        v = None
                    if v is None:
                        continue
                    line_sums[line_key] = line_sums.get(line_key, 0.0) + v
                    if "unit" in line and line_key not in line_units:
                        line_units[line_key] = line["unit"]
            out_financials[stmt] = {
                k: {"value": v, "unit": line_units.get(k, "USD")}
                for k, v in line_sums.items()
            }

        # Balance sheet = latest quarter snapshot.
        latest_bs = (
            (quarterly_records[0].get("financials") or {}).get("balance_sheet")
            or {}
        )
        out_financials["balance_sheet"] = dict(latest_bs)

        return {
            "financials": out_financials,
            "synthetic_ttm": True,
            "start_date": quarterly_records[3].get("start_date"),
            "end_date": quarterly_records[0].get("end_date"),
            "filing_date": quarterly_records[0].get("filing_date"),
        }

    @staticmethod
    def _pf_pick(
        record: dict[str, Any] | None,
        statement_key: str,
        line_keys: list[str],
    ) -> float | None:
        """Resolve `record.financials.<statement_key>.<line_keys[i]>.value`
        using the first matching key. Polygon line-item keys vary slightly
        across companies/forms so we try a list of candidates."""
        if not record:
            return None
        statement = (record.get("financials") or {}).get(statement_key) or {}
        for k in line_keys:
            line = statement.get(k)
            if line is not None:
                v = PolygonFinancialsProvider.value_of(line)
                if v is not None:
                    return v
        return None

    def _spot_close_on_or_before(
        self,
        symbol: str,
        as_of_date: str,
    ) -> float | None:
        """Daily close on the most recent trading day <= as_of_date.
        Reuses the existing daily-bar helper to avoid extra API calls."""
        if not symbol:
            return None
        try:
            close = self._get_last_close_on_or_before(symbol, as_of_date)
            return float(close) if close is not None else None
        except Exception:
            return None

    def _extract_statement_trends(
        self,
        ticker: Any,
        as_of_date: str | None = None,
    ) -> dict[str, float | None]:
        out: dict[str, float | None] = {}
        try:
            income = self._clip_statement_to_date(
                ticker.quarterly_income_stmt, as_of_date
            )
            cashflow = self._clip_statement_to_date(
                ticker.quarterly_cashflow, as_of_date
            )
        except Exception:
            return out

        out["revenue_qoq_growth"] = self._extract_qoq_growth(
            income, ["Total Revenue", "Revenue", "Operating Revenue"]
        )
        out["net_income_qoq_growth"] = self._extract_qoq_growth(
            income, ["Net Income", "Net Income Common Stockholders"]
        )
        out["operating_income_qoq_growth"] = self._extract_qoq_growth(
            income, ["Operating Income", "OperatingIncome"]
        )
        out["free_cashflow_qoq_growth"] = self._extract_qoq_growth(
            cashflow, ["Free Cash Flow", "FreeCashFlow"]
        )
        return out

    @staticmethod
    def _clip_statement_to_date(
        statement_df: pd.DataFrame | None,
        as_of_date: str | None,
    ) -> pd.DataFrame | None:
        if statement_df is None or statement_df.empty or not as_of_date:
            return statement_df
        try:
            cutoff = pd.Timestamp(as_of_date)
        except Exception:
            return statement_df
        cols = pd.to_datetime(statement_df.columns, errors="coerce")
        mask = (cols <= cutoff).to_numpy()
        if not mask.any():
            return statement_df.iloc[:, :0]
        return statement_df.loc[:, mask]

    def _extract_qoq_growth(
        self,
        statement_df: pd.DataFrame | None,
        row_candidates: list[str],
    ) -> float | None:
        if statement_df is None or statement_df.empty:
            return None
        row_name = next(
            (name for name in row_candidates if name in statement_df.index),
            None,
        )
        if row_name is None:
            return None

        row = pd.to_numeric(
            statement_df.loc[row_name], errors="coerce"
        ).dropna()
        if len(row) < 2:
            return None
        row = row.sort_index()
        latest = float(row.iloc[-1])
        previous = float(row.iloc[-2])
        if previous == 0:
            return None
        return (latest / previous) - 1.0

    def _load_news(
        self,
        ticker: Any,
        as_of_date: str,
    ) -> dict[str, Any]:
        symbol_str = (getattr(ticker, "ticker", "") or "").upper()
        cache_payload = {
            "symbol": symbol_str,
            "as_of_date": as_of_date,
            "provider": "yfinance_news",
        }
        cached = self._get_cached_json(
            "news",
            "symbol_news_summary",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if isinstance(cached, dict):
            self._set_pit("news", str(cached.get("pit_status", "unknown")))
            return cached
        try:
            raw_news = ticker.news or []
        except Exception:
            raw_news = []

        eod_epoch = self._as_of_eod_epoch(as_of_date)
        is_historical = self._resolve_pit_mode(as_of_date) == "historical"
        filtered: list[dict[str, Any]] = []
        items_missing_ts = 0
        for item in raw_news:
            ts = self._news_item_timestamp(item)
            if eod_epoch is None or ts is None:
                # Without a timestamp we can't safely call it PIT for a
                # historical date — drop it to avoid leakage.
                if is_historical:
                    items_missing_ts += 1
                    continue
                filtered.append(item)
                continue
            if ts <= eod_epoch:
                filtered.append(item)

        recent_titles: list[str] = []
        for item in filtered[:5]:
            title = item.get("title") or (item.get("content") or {}).get("title")
            if title:
                recent_titles.append(str(title).strip())

        if is_historical:
            # If we kept any items they were date-filtered → effectively PIT.
            # If we dropped everything because timestamps were missing we still
            # flag as live snapshot so the reader knows news coverage is empty.
            status = "pit" if filtered else "non_pit_live_snapshot"
        else:
            status = "live"
        self._set_pit("news", status)

        out = {
            "headlines": recent_titles,
            "count": len(filtered),
            "raw_count_before_pit_filter": len(raw_news),
            "items_dropped_missing_timestamp": items_missing_ts,
            "pit_status": status,
        }
        self._set_cached_json(
            "news",
            "symbol_news_summary",
            cache_payload,
            out,
        )
        return out

    def _load_news_sentiment(
        self,
        symbol: str,
        as_of_date: str,
        *,
        lookback_days: int = 14,
        limit: int = 50,
    ) -> dict[str, Any]:
        """Pull PIT-correct Polygon news + aggregate sentiment for the factor.

        Returns a dict shaped for `key_features.news_sentiment` with
        `status`, `pit_status`, `net_sentiment`, `n_articles`,
        `n_positive`/`n_negative`/`n_neutral`, `n_with_insights`,
        `n_keyword_fallback`, `window_start`, `window_end`,
        `lookback_days`, `source`.
        """
        empty = {
            "status": "unavailable",
            "pit_status": "unavailable",
            "net_sentiment": None,
            "n_articles": 0,
            "n_positive": 0,
            "n_negative": 0,
            "n_neutral": 0,
            "n_with_insights": 0,
            "n_keyword_fallback": 0,
            "window_start": None,
            "window_end": as_of_date,
            "lookback_days": int(lookback_days),
            "source": "polygon_news",
        }
        if not self.enable_news_factor:
            self._set_pit("news_sentiment", "disabled")
            return {**empty, "status": "disabled", "pit_status": "disabled"}
        if self.polygon_news_provider is None:
            self._set_pit("news_sentiment", "unavailable")
            return {**empty, "reason": "no_polygon_api_key"}

        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "lookback_days": int(lookback_days),
            "limit": int(limit),
            "provider": "polygon_news",
        }
        cached = self._get_cached_json(
            "news_sentiment",
            "sentiment_summary",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if isinstance(cached, dict):
            self._set_pit(
                "news_sentiment",
                str(cached.get("pit_status", "unknown")),
            )
            return cached

        # `published_utc.lte` accepts ISO dates; pass end-of-day to keep
        # any article published on `as_of_date` itself.
        published_before = f"{as_of_date}T23:59:59Z"
        try:
            raw_items = self.polygon_news_provider.get_news(
                symbol=symbol,
                limit=int(limit),
                published_before=published_before,
            )
        except Exception:
            raw_items = []

        agg = compute_news_sentiment(
            raw_items, as_of_date=as_of_date, lookback_days=int(lookback_days),
        )
        is_historical = self._resolve_pit_mode(as_of_date) == "historical"
        pit_status = "pit" if is_historical else "live"
        out = {
            "status": "ok" if agg["n_articles"] > 0 else "no_articles",
            "pit_status": pit_status,
            "net_sentiment": agg["net_sentiment"],
            "n_articles": agg["n_articles"],
            "n_positive": agg["n_positive"],
            "n_negative": agg["n_negative"],
            "n_neutral": agg["n_neutral"],
            "n_with_insights": agg["n_with_insights"],
            "n_keyword_fallback": agg["n_keyword_fallback"],
            "window_start": agg["window_start"],
            "window_end": agg["window_end"],
            "lookback_days": int(lookback_days),
            "source": "polygon_news",
        }
        self._set_pit("news_sentiment", pit_status)
        self._set_cached_json(
            "news_sentiment", "sentiment_summary", cache_payload, out,
        )
        return out

    def _load_intraday_context(
        self,
        symbol: str,
        as_of_date: str,
    ) -> dict[str, Any]:
        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "provider": self.data_provider,
        }
        cached = self._get_cached_json(
            "intraday",
            "context",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=300),
        )
        if isinstance(cached, dict):
            self._set_pit(
                "intraday_context",
                str(cached.get("pit_status", "unknown")),
            )
            return cached
        end_dt = datetime.strptime(as_of_date, "%Y-%m-%d")
        start_dt = end_dt - timedelta(days=7)
        df: pd.DataFrame | None = None
        if self.data_provider in ("auto", "polygon") and self.polygon_api_key:
            url = (
                "https://api.polygon.io/v2/aggs/ticker/"
                f"{symbol.upper()}/range/1/hour/"
                f"{start_dt.strftime('%Y-%m-%d')}/"
                f"{end_dt.strftime('%Y-%m-%d')}"
            )
            params = {
                "adjusted": "true",
                "sort": "asc",
                "limit": 50000,
                "apiKey": self.polygon_api_key,
            }
            try:
                response = requests.get(url, params=params, timeout=20)
                response.raise_for_status()
                rows = response.json().get("results") or []
                if rows:
                    df = pd.DataFrame(rows).rename(
                        columns={
                            "t": "Date",
                            "o": "Open",
                            "h": "High",
                            "l": "Low",
                            "c": "Close",
                            "v": "Volume",
                        }
                    )
                    df["Date"] = pd.to_datetime(
                        df["Date"],
                        unit="ms",
                        utc=True,
                    ).dt.tz_localize(None)
            except Exception:
                df = None
        if df is None or df.empty:
            try:
                # yfinance 60m intraday only goes back ~730 days; for old
                # `as_of_date` values this will return empty (handled below).
                fallback_end = end_dt + timedelta(days=1)
                fallback = yf.download(
                    symbol.upper(),
                    start=start_dt.strftime("%Y-%m-%d"),
                    end=fallback_end.strftime("%Y-%m-%d"),
                    interval="60m",
                    auto_adjust=True,
                    progress=False,
                    multi_level_index=False,
                )
                if not fallback.empty:
                    df = fallback.reset_index()
            except Exception:
                df = None
        if df is None or df.empty:
            self._set_pit("intraday_context", "unavailable")
            return {"status": "unavailable", "pit_status": "unavailable"}
        # Belt-and-suspenders: clip to as_of_date EOD if any rows slipped past.
        try:
            cutoff = pd.Timestamp(as_of_date) + pd.Timedelta(days=1)
            if "Date" in df.columns:
                df = df[df["Date"] < cutoff]
        except Exception:
            pass
        if df.empty:
            self._set_pit("intraday_context", "unavailable")
            return {"status": "unavailable", "pit_status": "unavailable"}
        self._set_pit("intraday_context", "pit")

        close = pd.to_numeric(df["Close"], errors="coerce")
        high = pd.to_numeric(df["High"], errors="coerce")
        low = pd.to_numeric(df["Low"], errors="coerce")
        volume = pd.to_numeric(df["Volume"], errors="coerce")
        returns = close.pct_change()
        intraday_rsi = self._compute_rsi(close.dropna(), period=14)
        latest_close = float(close.iloc[-1])
        typical = (high + low + close) / 3.0
        vwap = (
            float((typical * volume).sum() / volume.sum())
            if volume.sum() > 0
            else None
        )
        prev_day_high = (
            float(high.iloc[-24:-1].max()) if len(high) > 24 else None
        )
        prev_day_low = float(low.iloc[-24:-1].min()) if len(low) > 24 else None
        intraday_vol = (
            float(returns.tail(24).std())
            if len(returns.dropna()) > 10
            else None
        )
        out = {
            "status": "ok",
            "pit_status": "pit",
            "bars": int(len(df)),
            "latest_close": self._safe_float(latest_close),
            "intraday_rsi_14": self._safe_float(intraday_rsi),
            "vwap_proxy": self._safe_float(vwap),
            "prev_day_high": self._safe_float(prev_day_high),
            "prev_day_low": self._safe_float(prev_day_low),
            "intraday_vol_24h": self._safe_float(intraday_vol),
            "break_above_prev_day_high": (
                bool(latest_close > prev_day_high)
                if self._is_valid(prev_day_high)
                else None
            ),
            "break_below_prev_day_low": (
                bool(latest_close < prev_day_low)
                if self._is_valid(prev_day_low)
                else None
            ),
        }
        self._set_cached_json(
            "intraday",
            "context",
            cache_payload,
            out,
        )
        return out

    def _load_filings_context(
        self,
        symbol: str,
        as_of_date: str | None = None,
    ) -> dict[str, Any]:
        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "provider": "sec_submissions",
            # Segregate analysis-enriched entries from metadata-only ones so a
            # metadata-only cache hit is not reused when analysis is requested.
            "with_analysis": self.enable_filing_analysis,
        }
        cached = self._get_cached_json(
            "filings",
            "latest_filing",
            cache_payload,
            ttl_seconds=(
                self._cache_ttl(as_of_date, live_seconds=3600)
                if as_of_date
                else 3600
            ),
        )
        if isinstance(cached, dict) and cached.get("status") != "error":
            return cached
        try:
            latest = self.sec_filings_provider.get_latest_filing(
                symbol,
                as_of_date=as_of_date,
            )
        except SECFetchError as exc:
            # Transient fetch failure (rate-limit, 5xx, network). Do NOT
            # cache — historical-date entries have ttl=None, so a cached
            # error would persist forever.
            return {"status": "error", "reason": str(exc)}
        if not latest:
            # The submissions feed returned successfully but contained no
            # 10-Q/10-K/8-K filing on/before `as_of_date`. This is a real
            # PIT result and is safe to cache.
            out = {"status": "unavailable"}
            self._set_cached_json("filings", "latest_filing", cache_payload, out)
            return out
        out = {
            "status": "ok",
            "latest_form": latest.get("form"),
            "latest_accession": latest.get("accession"),
            "latest_filing_date": latest.get("filing_date"),
            "cik": latest.get("cik"),
        }
        if self.enable_filing_analysis:
            out["filing_analysis"] = self._run_filing_analysis(symbol, as_of_date)
        self._set_cached_json("filings", "latest_filing", cache_payload, out)
        return out


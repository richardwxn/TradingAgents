    def _load_regime_weights_map(self) -> dict[str, dict[str, float]] | None:
        if not self.regime_weights_path:
            return None
        if self._regime_weights_cache is not None:
            return self._regime_weights_cache
        try:
            payload = json.loads(Path(self.regime_weights_path).read_text())
        except Exception:
            self._regime_weights_cache = {}
            return self._regime_weights_cache
        cleaned: dict[str, dict[str, float]] = {}
        for regime, mapping in (payload or {}).items():
            if not isinstance(mapping, dict):
                continue
            row: dict[str, float] = {}
            for k, v in mapping.items():
                try:
                    row[str(k)] = float(v)
                except (TypeError, ValueError):
                    continue
            if row:
                cleaned[str(regime)] = row
        self._regime_weights_cache = cleaned
        return self._regime_weights_cache

    def _estimate_atm_iv_annual(
        self,
        ticker: Any,
        expiries: list[str],
        spot_price: float | None,
    ) -> float | None:
        if not self._is_valid(spot_price):
            return None
        iv_values: list[float] = []
        for expiry in expiries[:2]:
            try:
                chain = ticker.option_chain(expiry)
            except Exception:
                continue
            for table in (chain.calls, chain.puts):
                if table is None or table.empty:
                    continue
                local = table.copy()
                local["strike"] = pd.to_numeric(
                    local.get("strike"),
                    errors="coerce",
                )
                local["impliedVolatility"] = pd.to_numeric(
                    local.get("impliedVolatility"),
                    errors="coerce",
                )
                local = local.dropna(subset=["strike", "impliedVolatility"])
                if local.empty:
                    continue
                local["distance"] = (local["strike"] - spot_price).abs()
                atm = local.nsmallest(3, "distance")
                iv_values.extend(atm["impliedVolatility"].tolist())
        if not iv_values:
            return None
        return self._safe_float(float(pd.Series(iv_values).median()))


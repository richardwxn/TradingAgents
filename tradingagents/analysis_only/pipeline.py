    def run(self, symbol: str, as_of_date: str) -> AnalysisReport:
        symbol = symbol.upper()
        self._active_report_cache_key = self.report_cache_key(symbol, as_of_date)
        self._pit_status = {}
        self._prev_state = (
            self._state_store.get_symbol_state(symbol)
            if self._state_store
            else None
        )
        self._log(f"Starting analysis for {symbol} on {as_of_date}")
        ticker = yf.Ticker(symbol)
        self._log("Loading price data...")
        price_df = self._load_price_data(symbol, as_of_date, lookback_days=300)
        self._set_pit("price_data", "pit")
        self._log(
            "Loaded "
            f"{len(price_df)} price rows via provider={self.resolved_provider}"
        )
        self._log("Computing technical features...")
        technicals = self._compute_technical_features(price_df)
        self._log("Loading fundamentals...")
        fundamentals = self._load_fundamentals(
            ticker,
            as_of_date=as_of_date,
            spot_close=technicals.get("close"),
        )
        # Unusual-flow scan needs intraday/quote-level data we can't
        # reconstruct on the Polygon Starter plan, so it stays live-only.
        # The option *chain* (used by IV surface + strategies) CAN be
        # reconstructed PIT-correctly via the historical provider
        # (see `_load_option_chain_polygon_historical`), so it runs on
        # historical dates too.
        options_pit_mode = self._resolve_pit_mode(as_of_date)
        flow_enabled = self.options_enabled and options_pit_mode == "live"
        chain_enabled = self.options_enabled  # both live and historical
        if self.options_enabled and options_pit_mode == "historical":
            self._log(
                "Options flow scan disabled (live-only); "
                "option chain will be reconstructed PIT via historical provider."
            )
        else:
            self._log(
                "Scanning options flow..."
                if flow_enabled
                else "Options scan disabled."
            )
        if flow_enabled:
            options_flow = self._scan_unusual_options(
                ticker,
                symbol=symbol,
                spot_price=technicals.get("close"),
            )
            self._set_pit("options_flow", "live")
        else:
            options_flow = self._options_disabled_snapshot()
            self._set_pit(
                "options_flow",
                "disabled_historical" if self.options_enabled else "disabled",
            )
        if chain_enabled:
            option_chain_context = self._load_option_chain_for_strategies(
                ticker=ticker,
                symbol=symbol,
                as_of_date=as_of_date,
                spot_price=technicals.get("close"),
            )
            self._set_pit(
                "option_strategy_chain",
                str(option_chain_context.get("pit_status") or "unavailable"),
            )
        else:
            option_chain_context = {
                "status": "disabled",
                "pit_status": "disabled",
                "contracts": [],
            }
            self._set_pit("option_strategy_chain", "disabled")
        if self.enable_news_fetching:
            self._log("Loading recent news...")
            news_summary = self._load_news(ticker, as_of_date=as_of_date)
        else:
            self._log("News fetch skipped (enable_news_fetching=False).")
            news_summary = {"count": 0, "items": [], "status": "disabled"}
            self._set_pit("news", "disabled")
        # X1: Polygon news-sentiment factor. PIT-correct via
        # `published_utc.lte=as_of_date`; works on historical regens even
        # when `enable_news_fetching` is False.
        if self.enable_news_factor:
            self._log("Loading Polygon news sentiment...")
            news_sentiment_block = self._load_news_sentiment(
                symbol=symbol, as_of_date=as_of_date,
            )
        else:
            self._log("News sentiment factor skipped (enable_news_factor=False).")
            news_sentiment_block = {
                "status": "disabled",
                "pit_status": "disabled",
                "net_sentiment": None,
                "n_articles": 0,
                "n_positive": 0,
                "n_negative": 0,
                "n_neutral": 0,
                "n_with_insights": 0,
                "n_keyword_fallback": 0,
                "window_start": None,
                "window_end": as_of_date,
                "lookback_days": 14,
                "source": "polygon_news",
            }
            self._set_pit("news_sentiment", "disabled")
        if self.enable_intraday_context:
            self._log("Loading intraday trigger context...")
            intraday_context = self._load_intraday_context(
                symbol=symbol,
                as_of_date=as_of_date,
            )
        else:
            self._log(
                "Intraday context skipped (enable_intraday_context=False)."
            )
            intraday_context = {
                "status": "disabled",
                "pit_status": "disabled",
            }
            self._set_pit("intraday_context", "disabled")
        self._log("Loading market regime context...")
        market_context = self._load_market_context(as_of_date=as_of_date)
        self._set_pit("market_context", "pit")
        self._set_pit(
            "market_context.fear_greed",
            str(market_context.get("fear_greed_pit_status") or "unavailable"),
        )
        if self.enable_filings_fetching:
            self._log("Loading filings context...")
            filings_context = self._load_filings_context(
                symbol=symbol,
                as_of_date=as_of_date,
            )
            self._set_pit("filings_context", "pit")
        else:
            self._log("Filings fetch skipped (enable_filings_fetching=False).")
            filings_context = {
                "status": "disabled",
                "latest_form": None,
                "latest_filing_date": None,
            }
            self._set_pit("filings_context", "disabled")
        self._log("Building industry and event context...")
        industry_context = self._load_industry_context(
            symbol=symbol,
            as_of_date=as_of_date,
            ticker=ticker,
        )
        company_events = self._load_company_events(
            news_summary,
            ticker,
            as_of_date=as_of_date,
        )
        event_timeline = self._build_event_timeline(
            news_summary=news_summary,
            filings_context=filings_context,
            as_of_date=as_of_date,
        )
        self._set_pit(
            "event_timeline",
            self._pit_status.get("news", "pit"),
        )
        if self.enable_peer_competitor_analysis:
            self._log("Running competitor benchmark...")
            competitor_analysis = self._build_competitor_analysis(
                symbol=symbol,
                as_of_date=as_of_date,
                close=technicals.get("close"),
                fundamentals=fundamentals,
                industry_context=industry_context,
            )
        else:
            self._log(
                "Peer competitor analysis skipped "
                "(enable_peer_competitor_analysis=False)."
            )
            competitor_analysis = {
                "status": "disabled",
                "peer_tickers": [],
                "peer_metrics": [],
                "summary": {},
                "peer_fundamentals_pit_status": "disabled",
            }
            self._set_pit("competitor_analysis", "disabled")
            self._set_pit(
                "competitor_analysis.peer_fundamentals",
                "disabled",
            )
        self._log("Loading industry news context...")
        industry_news_context = self._load_industry_news_context(
            symbol=symbol,
            as_of_date=as_of_date,
            industry_context=industry_context,
            competitor_analysis=competitor_analysis,
        )
        self._log("Loading earnings calendar...")
        earnings_calendar = self._load_earnings_calendar(
            symbol=symbol,
            ticker=ticker,
            as_of_date=as_of_date,
        )
        self._log("Loading analyst consensus...")
        analyst_consensus = self._load_analyst_consensus(
            symbol=symbol,
            ticker=ticker,
            as_of_date=as_of_date,
            close=technicals.get("close"),
        )
        portfolio_context = self._portfolio_context(symbol)
        self._log("Building final analysis report...")

        report = self._build_report(
            symbol=symbol,
            as_of_date=as_of_date,
            technicals=technicals,
            fundamentals=fundamentals,
            options_flow=options_flow,
            option_chain_context=option_chain_context,
            news_summary=news_summary,
            news_sentiment_block=news_sentiment_block,
            intraday_context=intraday_context,
            market_context=market_context,
            filings_context=filings_context,
            industry_context=industry_context,
            company_events=company_events,
            event_timeline=event_timeline,
            competitor_analysis=competitor_analysis,
            industry_news_context=industry_news_context,
            earnings_calendar=earnings_calendar,
            analyst_consensus=analyst_consensus,
            portfolio_context=portfolio_context,
            price_rows=len(price_df),
        )
        if self._state_store:
            self._persist_state_for(symbol=symbol, report=report)
        self._log("Analysis complete.")
        return report


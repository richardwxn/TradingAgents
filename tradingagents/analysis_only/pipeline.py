    def _load_option_chain_polygon(
        self,
        symbol: str,
        as_of_date: str,
        spot_price: float | None,
    ) -> dict[str, Any]:
        cache_payload = {
            "symbol": symbol.upper(),
            "as_of_date": as_of_date,
            "spot_price": round(float(spot_price), 6)
            if self._is_valid(spot_price)
            else None,
            "provider": "polygon_options_snapshot",
        }
        cached = self._get_cached_json(
            "options",
            "snapshot_chain",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if isinstance(cached, dict):
            return cached
        if not self.polygon_api_key:
            return {
                "status": "unavailable",
                "source": "polygon_options_snapshot",
                "pit_status": "unavailable",
                "contracts": [],
                "reason": "POLYGON_API_KEY not configured.",
            }

        all_results: list[dict[str, Any]] = []
        next_url = f"https://api.polygon.io/v3/snapshot/options/{symbol.upper()}"
        params: dict[str, Any] | None = {
            "limit": 250,
            "apiKey": self.polygon_api_key,
        }
        page_count = 0
        while next_url and page_count < 10:
            page_count += 1
            try:
                response = requests.get(next_url, params=params, timeout=20)
                response.raise_for_status()
                payload = response.json()
            except Exception as exc:
                return {
                    "status": "error",
                    "source": "polygon_options_snapshot",
                    "pit_status": self._live_or_leak(as_of_date),
                    "contracts": [],
                    "error": str(exc),
                }
            all_results.extend(payload.get("results") or [])
            next_url = payload.get("next_url")
            params = None
            if next_url and "apiKey=" not in next_url:
                sep = "&" if "?" in next_url else "?"
                next_url = f"{next_url}{sep}apiKey={self.polygon_api_key}"

        contracts = [
            c for c in (
                self._normalize_polygon_option_row(
                    row,
                    as_of_date=as_of_date,
                    spot_price=spot_price,
                )
                for row in all_results
            )
            if c is not None
        ]
        if not contracts:
            return {
                "status": "unavailable",
                "source": "polygon_options_snapshot",
                "pit_status": self._live_or_leak(as_of_date),
                "contracts": [],
                "reason": "No usable option contracts in Polygon snapshot.",
            }
        out = {
            "status": "ok",
            "source": "polygon_options_snapshot",
            "pit_status": self._live_or_leak(as_of_date),
            "contracts": contracts,
            "contracts_scanned": len(all_results),
            "contracts_usable": len(contracts),
        }
        self._set_cached_json(
            "options",
            "snapshot_chain",
            cache_payload,
            out,
        )
        return out

    def _load_option_chain_yfinance(
        self,
        ticker: Any,
        as_of_date: str,
        spot_price: float | None,
        max_expiries: int = 12,
    ) -> dict[str, Any]:
        symbol_str = (getattr(ticker, "ticker", "") or "").upper()
        cache_payload = {
            "symbol": symbol_str,
            "as_of_date": as_of_date,
            "spot_price": round(float(spot_price), 6)
            if self._is_valid(spot_price)
            else None,
            "max_expiries": max_expiries,
            "provider": "yfinance_options_live",
        }
        cached = self._get_cached_json(
            "options",
            "yfinance_chain",
            cache_payload,
            ttl_seconds=self._cache_ttl(as_of_date, live_seconds=900),
        )
        if isinstance(cached, dict):
            return cached
        try:
            expiries = list(ticker.options or [])
        except Exception as exc:
            return {
                "status": "error",
                "source": "yfinance_options_live",
                "pit_status": self._live_or_leak(as_of_date),
                "contracts": [],
                "error": str(exc),
            }
        contracts: list[dict[str, Any]] = []
        scanned = 0
        for expiry in expiries[:max_expiries]:
            dte = self._option_dte(expiry, as_of_date)
            if dte is None or dte < 7 or dte > 760:
                continue
            try:
                chain = ticker.option_chain(expiry)
            except Exception:
                continue
            for option_type, table in (("call", chain.calls), ("put", chain.puts)):
                if table is None or table.empty:
                    continue
                scanned += len(table)
                for _, row in table.iterrows():
                    contract = self._normalize_yfinance_option_row(
                        row=row,
                        option_type=option_type,
                        expiry=expiry,
                        dte=dte,
                        spot_price=spot_price,
                    )
                    if contract is not None:
                        contracts.append(contract)
        status = "ok" if contracts else "unavailable"
        out = {
            "status": status,
            "source": "yfinance_options_live",
            "pit_status": self._live_or_leak(as_of_date),
            "contracts": contracts,
            "contracts_scanned": scanned,
            "contracts_usable": len(contracts),
        }
        if status == "ok":
            self._set_cached_json(
                "options",
                "yfinance_chain",
                cache_payload,
                out,
            )
        return out

    def _normalize_polygon_option_row(
        self,
        row: dict[str, Any],
        as_of_date: str,
        spot_price: float | None,
    ) -> dict[str, Any] | None:
        details = row.get("details") or {}
        day = row.get("day") or {}
        quote = row.get("last_quote") or {}
        greeks = row.get("greeks") or {}
        option_type = details.get("contract_type")
        expiry = details.get("expiration_date")
        strike = self._safe_float(details.get("strike_price"))
        dte = self._option_dte(expiry, as_of_date)
        if (
            option_type not in {"call", "put"}
            or not expiry
            or not self._is_valid(strike)
            or dte is None
            or dte < 7
            or dte > 760
        ):
            return None
        bid = self._safe_float(
            quote.get("bid") or quote.get("bid_price") or quote.get("bp")
        )
        ask = self._safe_float(
            quote.get("ask") or quote.get("ask_price") or quote.get("ap")
        )
        last = self._safe_float(day.get("close") or day.get("vwap"))
        mid = (
            self._safe_float((bid + ask) / 2.0)
            if self._is_valid(bid)
            and self._is_valid(ask)
            and (ask or 0.0) > 0
            else last
        )
        if not self._is_valid(mid):
            return None
        return {
            "contract_symbol": details.get("ticker"),
            "type": option_type,
            "expiry": expiry,
            "dte": dte,
            "strike": strike,
            "bid": bid,
            "ask": ask,
            "mid": mid,
            "last": last,
            "volume": int(self._safe_float(day.get("volume")) or 0),
            "open_interest": int(self._safe_float(row.get("open_interest")) or 0),
            "implied_volatility": self._safe_float(row.get("implied_volatility")),
            "delta": self._safe_float(greeks.get("delta")),
            "gamma": self._safe_float(greeks.get("gamma")),
            "theta": self._safe_float(greeks.get("theta")),
            "vega": self._safe_float(greeks.get("vega")),
            "spot_distance_pct": (
                self._safe_float((strike - spot_price) / spot_price)
                if self._is_valid(spot_price)
                and spot_price
                else None
            ),
        }


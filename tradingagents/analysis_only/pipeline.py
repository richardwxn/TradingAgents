@dataclass
class AnalysisReport:
    symbol: str
    horizon: str
    as_of_date: str
    thesis: str
    direction: str
    confidence: float
    bull_case: list[str]
    bear_case: list[str]
    key_features: dict[str, Any]
    risk_flags: list[str]
    invalidation_conditions: list[str]
    data_quality: dict[str, Any]
    generated_at_utc: str
    # Phase 6: optional adversarial LLM critic block. Top-level so
    # downstream loaders (`backtest.load_records`, `scripts/backfill_llm_critic.py`)
    # find it at the same path regardless of whether the report was
    # generated by the live pipeline or backfilled offline.
    llm_critic: dict[str, Any] | None = None

    def to_json_dict(self) -> dict[str, Any]:
        return asdict(self)



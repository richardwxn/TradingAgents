    def _portfolio_context(self, symbol: str) -> dict[str, Any]:
        snapshot = self._portfolio_snapshot or {}
        if snapshot.get("status") != "ok":
            return {"status": snapshot.get("status", "disabled")}
        account = dict(snapshot.get("account") or {})
        total_equity = self._safe_float(account.get("total_equity"))
        cash = self._safe_float(account.get("cash"))
        margin_limit = self._safe_float(account.get("margin_limit"))
        short_put_margin_reserved = self._safe_float(
            account.get("short_put_margin_reserved")
        )
        margin_remaining = (
            margin_limit - short_put_margin_reserved
            if self._is_valid(margin_limit)
            and self._is_valid(short_put_margin_reserved)
            else None
        )
        short_put_util = (
            short_put_margin_reserved / margin_limit
            if self._is_valid(margin_limit)
            and self._is_valid(short_put_margin_reserved)
            and float(margin_limit) > 0
            else None
        )
        account.update(
            {
                "total_equity": total_equity,
                "cash": cash,
                "cash_pct": (
                    self._safe_float(cash / total_equity)
                    if self._is_valid(cash)
                    and self._is_valid(total_equity)
                    and float(total_equity) > 0
                    else None
                ),
                "margin_limit": margin_limit,
                "short_put_margin_reserved": short_put_margin_reserved,
                "margin_remaining": self._safe_float(margin_remaining),
                "short_put_margin_utilization": self._safe_float(short_put_util),
            }
        )
        positions = snapshot.get("positions") or []
        holding: dict[str, Any] = {"has_position": False, "symbol": symbol.upper()}
        visible_equity = 0.0
        concentration: list[dict[str, Any]] = []
        for pos in positions:
            if not isinstance(pos, dict):
                continue
            equity = self._safe_float(pos.get("equity"))
            visible_equity += equity or 0.0
            p_symbol = str(pos.get("symbol") or "").upper()
            if p_symbol:
                concentration.append(
                    {
                        "symbol": p_symbol,
                        "equity": equity,
                        "portfolio_weight": (
                            self._safe_float(equity / total_equity)
                            if self._is_valid(equity)
                            and self._is_valid(total_equity)
                            and float(total_equity) > 0
                            else None
                        ),
                    }
                )
            if p_symbol == symbol.upper():
                holding = dict(pos)
                holding["has_position"] = True
                holding["portfolio_weight"] = (
                    self._safe_float(equity / total_equity)
                    if self._is_valid(equity)
                    and self._is_valid(total_equity)
                    and float(total_equity) > 0
                    else None
                )
                avg = self._safe_float(holding.get("average_cost"))
                price = self._safe_float(holding.get("price"))
                holding["unrealized_return_pct"] = (
                    self._safe_float((price / avg) - 1.0)
                    if self._is_valid(price)
                    and self._is_valid(avg)
                    and float(avg) > 0
                    else None
                )
        concentration = sorted(
            concentration,
            key=lambda item: item.get("equity") or 0.0,
            reverse=True,
        )
        return {
            "status": "ok",
            "source": snapshot.get("source"),
            "as_of": snapshot.get("as_of"),
            "account": account,
            "holding": holding,
            "visible_positions_equity": self._safe_float(visible_equity),
            "top_concentration": concentration[:5],
        }

